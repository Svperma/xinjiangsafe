package com.dsib.entity;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * 评价对象
 * 
 * @author xinjg
 * 
 */
public class GgEvaluate implements Serializable  {

	private String seriesNo;
	// 被评价/投诉/建议人
	private String beEvaluator;
	// 评价/投诉/建议 类型：1保单，2理赔，3其他

	private String item;
	private String content;
	private String score;// 得分
	private String evaluator;// 评价、投诉、建议人
	private Date evaluateTime;
	private String AttributeId;// 1评价，2回复，3建议，4投诉
	private String markId;
	private String province;
	private String city;
	private String county;
	private String remark;
	private String flag;
	private List<GgEvaluate> list;

	public String getSeriesNo() {
		return seriesNo;
	}

	public void setSeriesNo(String seriesNo) {
		this.seriesNo = seriesNo;
	}

	public String getBeEvaluator() {
		return beEvaluator;
	}

	public void setBeEvaluator(String beEvaluator) {
		this.beEvaluator = beEvaluator;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getEvaluator() {
		return evaluator;
	}

	public void setEvaluator(String evaluator) {
		this.evaluator = evaluator;
	}

	public Date getEvaluateTime() {
		return evaluateTime;
	}

	public void setEvaluateTime(Date evaluateTime) {
		this.evaluateTime = evaluateTime;
	}

	public String getAttributeId() {
		return AttributeId;
	}

	public void setAttributeId(String attributeId) {
		AttributeId = attributeId;
	}

	public String getMarkId() {
		return markId;
	}

	public void setMarkId(String markId) {
		this.markId = markId;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public List<GgEvaluate> getList() {
		return list;
	}

	public void setList(List<GgEvaluate> list) {
		this.list = list;
	}

}
